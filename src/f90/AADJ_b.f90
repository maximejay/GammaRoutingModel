    IMPLICIT NONE
    TYPE(TYPE_ROUTING_MESH), INTENT(IN) :: routing_mesh
    TYPE(TYPE_ROUTING_STATES), INTENT(IN) :: routing_states
    INTRINSIC SIZE
    TYPE(ROUTING_MEMORY), DIMENSION(SIZE(routing_states%quantile), &
&   routing_mesh%nb_nodes), INTENT(IN) :: routingmem
    TYPE(ROUTING_MEMORY), DIMENSION(SIZE(routing_states%quantile), &
